def modify_dictionary() -> None:
    """辞書 CSV を読み込み、修正して上書き保存する。"""

    with open(DICT_PATH, encoding="utf-8", newline="") as f:
        rows = list(csv.reader(f))

    print(f"Read {len(rows)} entries from {DICT_PATH.name}")

    # 既存の (surface, left_id, right_id, read) を収集（重複追加防止用）
    # (surface, read) だけでは POS 違いの同音エントリ（千切れ: 動詞未然形/連用形/名詞 等）を
    # 区別できないため、MeCab の文脈 ID (left_id, right_id) を含めて識別する
    # 複合語アクセントのコロン（例: ジシン:フセツ）は除去して正規化する
    existing_keys: set[tuple[str, str, str, str]] = set()
    for row in rows:
        existing_keys.add((row[0], row[1], row[2], _normalize_key_field(row[11])))

    # --- 読み修正 ---
    fix_count = 0
    for i, row in enumerate(rows):
        key = (row[0], row[11])
        if key in READING_FIXES:
            fixes = READING_FIXES[key]
            if "read" in fixes:
                rows[i][11] = fixes["read"]
            if "pron" in fixes:
                rows[i][12] = fixes["pron"]
            if "acc" in fixes:
                rows[i][13] = fixes["acc"]
            fix_count += 1
            # 読み修正後のキーを更新
            existing_keys.discard((row[0], row[1], row[2], _normalize_key_field(key[1])))
            existing_keys.add((row[0], row[1], row[2], _normalize_key_field(rows[i][11])))
    print(f"Applied {fix_count} reading fixes")

    # --- 活用形のコスト調整（プレフィックスマッチ） ---
    # 各行に対して最も specific（prefix が長い）ルールのみを適用する
    # COST_ADJUSTMENTS に exact match がある行はそちらが優先されるためスキップする
    cost_count = 0

    # 各行に対する最も specific な CONJ ルールを決定
    # キー: 行インデックス, 値: (specificity, new_cost)
    # specificity = surface_prefix の長さ + read_prefix の長さ（長いほど specific）
    conj_best: dict[int, tuple[int, int]] = {}
    for surface_prefix, orig_prefix, read_prefix, new_cost, _ in CONJUGATION_COST_ADJUSTMENTS:
        for i, row in enumerate(rows):
            if (
                row[0].startswith(surface_prefix)
                and row[10].startswith(orig_prefix)
                and row[11].startswith(read_prefix)
            ):
                # COST_ADJUSTMENTS に exact match がある行はスキップ
                if (row[0], row[11]) in COST_ADJUSTMENTS:
                    continue
                specificity = len(surface_prefix) + len(read_prefix)
                prev = conj_best.get(i)
                if prev is None or specificity > prev[0]:
                    conj_best[i] = (specificity, new_cost)

    for i, (_, new_cost) in conj_best.items():
        if rows[i][3] != str(new_cost):
            rows[i][3] = str(new_cost)
            cost_count += 1

    # --- 柄 (接尾辞のガラも下げる) ---
    # COST_ADJUSTMENTS の ("柄", "ガラ") は名詞,一般 (left_id=1345) にのみ適用される
    # 接尾辞 (pos="接尾") は left_id が異なるため別途ここで同じコストに揃える
    for i, row in enumerate(rows):
        if row[0] == "柄" and row[11] == "ガラ" and row[5] == "接尾":
            if row[3] != "4000":
                row[3] = "4000"
                cost_count += 1

    # --- コスト調整 (surface, read): 具体的なルールなのでプレフィックスルールの後に適用 ---
    # NEW_ENTRIES が管理するエントリは NEW_ENTRIES 側でコストが確定するため、ここではスキップする
    # (surface, read) のみでマッチする COST_ADJUSTMENTS が、異なる left_id の NEW_ENTRIES エントリまで
    # 巻き込んで上書きしてしまう問題を防ぐ
    new_entries_keys: set[tuple[str, str, str, str]] = set()
    for entry in NEW_ENTRIES:
        parsed = next(iter(csv.reader([entry])))
        new_entries_keys.add((parsed[0], parsed[1], parsed[2], _normalize_key_field(parsed[11])))
    for i, row in enumerate(rows):
        key = (row[0], row[11])
        if key in COST_ADJUSTMENTS:
            # NEW_ENTRIES で管理されているエントリはスキップ
            ne_key = (row[0], row[1], row[2], _normalize_key_field(row[11]))
            if ne_key in new_entries_keys:
                continue
            new_cost = str(COST_ADJUSTMENTS[key])
            if row[3] != new_cost:
                row[3] = new_cost
                cost_count += 1

    # --- 蓮の特殊コスト調整 ---
    for surface, left_id, read, old_cost_max, new_cost in SPECIAL_COST_ADJUSTMENTS:
        for i, row in enumerate(rows):
            if (
                row[0] == surface
                and row[1] == left_id
                and row[11] == read
                and int(row[3]) <= old_cost_max
            ):
                if row[3] != str(new_cost):
                    row[3] = str(new_cost)
                    cost_count += 1

    print(f"Applied {cost_count} cost adjustments")

    # --- 新規エントリ追加・既存エントリ上書き ---
    # キーが一致するエントリが既に CSV に存在する場合、NEW_ENTRIES の内容で丸ごと上書きする
    # これにより、アクセント等のフィールドをスクリプト側で変更しても再実行で反映される
    #
    # 複合語アクセント対応: read フィールドにコロン区切り（例: ジシン:フセツ）が含まれる場合、
    # コロンを除去した値（ジシンフセツ）でもマッチさせる。
    # これにより、NEW_ENTRIES 側でコロン有無を変更しても、既存エントリと正しく照合される。
    add_count = 0
    update_count = 0
    # 既存エントリの行インデックスを逆引きするマップ（識別キー → 行インデックス配列）
    # 同一 surface / read でも orig やアクセントが異なる既存行があるため、
    # 1 対 1 と仮定せずに候補配列で保持し、曖昧な場合は安全側で append する
    existing_identity_to_indices: dict[tuple[str, str, str, str, str], list[int]] = {}
    for i, row in enumerate(rows):
        identity_key = _entry_identity_key(row)
        existing_identity_to_indices.setdefault(identity_key, []).append(i)
    for entry in NEW_ENTRIES:
        parsed = next(iter(csv.reader([entry])))
        identity_key = _entry_identity_key(parsed)
        candidate_indices = existing_identity_to_indices.get(identity_key, [])
        if len(candidate_indices) == 1:
            # 既存エントリを上書き（内容が異なる場合のみカウント）
            idx = candidate_indices[0]
            if rows[idx] != parsed:
                rows[idx] = parsed
                update_count += 1
        elif len(candidate_indices) > 1:
            exact_match_idx = next((idx for idx in candidate_indices if rows[idx] == parsed), None)
            if exact_match_idx is None:
                rows.append(parsed)
                # identity_key は candidate_indices > 1 なので必ず存在する
                existing_identity_to_indices[identity_key].append(len(rows) - 1)
                add_count += 1
        else:
            rows.append(parsed)
            existing_identity_to_indices.setdefault(identity_key, []).append(len(rows) - 1)
            add_count += 1

    print(f"Added {add_count} new entries, updated {update_count} existing entries")

    # --- surface でソートして書き込み（末尾改行除去） ---
    rows.sort(key=lambda row: row[0])
    buf = io.StringIO()
    writer = csv.writer(buf, lineterminator="\n")
    writer.writerows(rows)
    content = buf.getvalue().rstrip("\n")

    with open(DICT_PATH, "w", encoding="utf-8", newline="") as f:
        f.write(content)

    print(f"Wrote {len(rows)} entries to {DICT_PATH.name}")


if __name__ == "__main__":
    modify_dictionary()
