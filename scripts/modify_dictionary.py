def _normalize_key_field(value: str) -> str:
    """辞書照合用にコロン区切りアクセントを正規化する。"""

    return value.replace(":", "")


def _entry_identity_key(row: list[str]) -> tuple[str, str, str, str, str]:
    """新規エントリ更新時に使う安全寄りの識別キーを返す。"""

    return (
        row[0],
        row[1],
        row[2],
        _normalize_key_field(row[10]),
        _normalize_key_field(row[11]),
    )


